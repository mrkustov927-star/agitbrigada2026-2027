-- Контрольные запросы. После выполнения должны быть значения:
-- events = 12, publications plan = 57, views plan = 52000,
-- participants plan = 1000, budget = 920005.00.
select
  p.name,
  p.plan_events,
  count(distinct e.id) as events_loaded,
  sum(e.plan_publications) as publications_plan_from_calendar,
  sum(e.plan_views) as views_plan_from_calendar,
  p.plan_unique_participants,
  p.plan_budget,
  (select sum(b.planned_amount) from public.budget_items b where b.project_id = p.id) as budget_loaded
from public.projects p
left join public.events e on e.project_id = p.id
where p.code = 'AGITBRIGADA-2026-2027'
group by p.id;
